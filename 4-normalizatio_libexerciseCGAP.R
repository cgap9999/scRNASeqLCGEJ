summary(lib.sf.zeisel)
hist(log10(lib.sf.zeisel), xlab = "Log10[Size factor]", col = "grey80")
ls.zeisel <- colSums(counts(sce.zeisel))
plot(
  ls.zeisel,
  lib.sf.zeisel,
  log = "xy",
  xlab = "Library size",
  ylab = "Size factor"
)

